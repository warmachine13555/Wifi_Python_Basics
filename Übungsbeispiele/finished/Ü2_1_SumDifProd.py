# Schrieben Sie ein Programm, dass von zwei einzugebenden Zahlen deren Summe, Differenz und Produkt ausgibt.

# Eingabe

zahl1 = float(input("Erste Zahl: "))
zahl2 = float(input("Zweite Zahl: "))

# Berechnung

sum = zahl1 + zahl2
diff = zahl1 - zahl2
prod = zahl1 * zahl2

# Ausgabge
print(f"Die Summer der zahlen beträgt: {sum} die Differenz beträgt: {diff} und das Produkt ist: {prod}")